# 3.task
# Задание: Напишите программу, которая запрашивает у пользователя номер дня недели (от 1 до 7) и выводит название соответствующего дня недели. Например, если пользователь вводит 1, программа должна вывести "Понедельник". Если введённое число не попадает в указанный диапазон, программа выводит сообщение об ошибке.
day_of_the_week = int(input("День недели (от 1 до 7): "))
if(day_of_the_week == 1):
    print("Понедельник")
elif(day_of_the_week == 2):
    print("Вторник")
elif(day_of_the_week == 3):
    print("Среда")
elif(day_of_the_week == 4):
    print("Четверг")
elif(day_of_the_week == 5):
    print("Пятница")
elif(day_of_the_week == 6):
    print("Субота")
elif(day_of_the_week == 7):
    print("Воскресение")
else:
    print("Ошибка")

