# 7.task
# Напишите программу, которая принимает время в формате 24-часового времени (0-23) и выводит, утро, день, вечер или ночь.
hour = int(input("Введите время (0-23): "))
if 0 <= hour < 6:
    print("Ночь")
elif 6 <= hour < 12:
    print("Утро")
elif 12 <= hour < 18:
    print("День")
else:
    print("Вечер")

